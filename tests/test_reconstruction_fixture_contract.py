def test_fixture_physical_cause_evidence_has_audit_fields():
    """Guards the item-3 stabilization: stored cause evidence must carry the
    full audit trail (model_version, feature_version, calibration, timing,
    raw/calibrated probabilities, applied caps) — not just a bare label."""
    data = _load_fixture()
    physical_cause = data["reconstruction"]["physical_cause_evidence"]
    assert physical_cause["scope"] == "RECORD_LOCAL_SIGNATURES"
    assert physical_cause["incident_root_cause"] == "UNCONFIRMED"
    assert physical_cause["records"], "fixture must contain per-record cause evidence"

    for record_entry in physical_cause["records"]:
        for field in (
            "analysis_id", "incident_record_id", "top_hypothesis", "confidence",
            "cause_ranking", "model_version", "feature_version", "calibration_method",
            "timing_source", "raw_probabilities", "calibrated_probabilities", "applied_caps",
        ):
            assert field in record_entry, f"physical_cause_evidence record missing field: {field}"

        # cause_ranking[0]'s confidence must match the capped top-level
        # confidence (regression guard for the ranking/confidence cap
        # consistency fix applied in ml_predict.py).
        if record_entry["cause_ranking"]:
            assert record_entry["confidence"] == record_entry["cause_ranking"][0]["confidence"]


def test_fixture_no_probability_averaging_field():
    """There must be no incident-level combined-probability field anywhere —
    only per-record entries plus a qualitative consistency label."""
    data = _load_fixture()
    physical_cause = data["reconstruction"]["physical_cause_evidence"]
    assert "incident_probability" not in physical_cause
    assert "average_confidence" not in physical_cause
    assert physical_cause["consistency"] in ("CONSISTENT", "MOSTLY_CONSISTENT", "MIXED", "CONTRADICTORY", "INSUFFICIENT")

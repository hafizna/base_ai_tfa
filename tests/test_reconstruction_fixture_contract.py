"""Contract test for the stable Stage 2 reconstruction API fixture.

``tests/fixtures/reconstruction_response.json`` is a real, ID-normalized
snapshot of a live POST /api/incidents/{id}/reconstruct response (plus its
companion timeline/relationships/episodes/records endpoints), generated by
``tests/fixtures/generate_reconstruction_fixture.py``. The frontend uses this
same file as mock data for Stage 2B component tests.

This test asserts the fixture's shape matches what the backend actually
returns today, so frontend contract tests built against it stay honest —
if the backend response shape changes, this test (and the frontend tests
built on the same fixture) should fail loudly rather than silently drift.
"""

from __future__ import annotations

import json
from pathlib import Path

FIXTURE_PATH = Path(__file__).parent / "fixtures" / "reconstruction_response.json"


def _load_fixture() -> dict:
    return json.loads(FIXTURE_PATH.read_text(encoding="utf-8"))


def test_fixture_file_exists_and_parses():
    assert FIXTURE_PATH.exists()
    data = _load_fixture()
    assert isinstance(data, dict)


def test_fixture_top_level_shape():
    data = _load_fixture()
    for key in ("incident_id", "upload", "incident", "reconstruction", "reconstructions", "timeline", "relationships", "episodes", "records"):
        assert key in data, f"missing top-level key: {key}"


def test_fixture_reconstruction_shape_matches_reconstruction_to_dict():
    from webapp.api.incidents.models import Reconstruction

    data = _load_fixture()
    recon = data["reconstruction"]

    # Every field on the dataclass's to_dict() must be present in the fixture
    # (plus the router's added timeline/relationships/episodes expansion).
    dummy = Reconstruction(reconstruction_id="x", incident_id="y")
    for key in dummy.to_dict():
        assert key in recon, f"reconstruction fixture missing field: {key}"

    for key in ("timeline", "relationships", "episodes"):
        assert key in recon, f"reconstruction fixture missing expanded field: {key}"


def test_fixture_alignment_shape():
    from webapp.api.incidents.models import AlignmentAssessment

    data = _load_fixture()
    alignment = data["reconstruction"]["alignment"]
    dummy = AlignmentAssessment()
    for key in dummy.to_dict():
        assert key in alignment, f"alignment fixture missing field: {key}"


